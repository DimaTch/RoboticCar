/* USER CODE BEGIN 0 */

void direct(uint8_t dir){

    GPIOA->BSRR |= GPIO_BSRR_BR4;   /* Enable DIR_EN */
    GPIOA->BSRR |= GPIO_BSRR_BR11;  /*  DIR_LATCH LOW */
    GPIOA->BSRR |= GPIO_BSRR_BR1;   /*  DIR_CLK_LOW */
  for(int i=0; i<=7; i++){
    GPIOA->BSRR |= GPIO_BSRR_BR1;   /*  DIR_CLK_LOW */
  //  LL_mDelay(10);
    if (dir&0x01U==1){
      GPIOA->BSRR |= GPIO_BSRR_BS5; /*  DIR_SER_HIGHT */
    }
    else{
      GPIOA->BSRR |= GPIO_BSRR_BR5;  /*  DIR_SER_LOW */
    }
    GPIOA->BSRR |= GPIO_BSRR_BS1;   /*  DIR_CLK_HIGHT */
    dir>>=0x01U;
 //   LL_mDelay(10);
  }
    GPIOA->BSRR |= GPIO_BSRR_BS11;  /*  DIR_LATCH HIGHT */
 //   LL_mDelay(10);
    GPIOA->BSRR |= GPIO_BSRR_BR11;  /*  DIR_LATCH LOW */
}

